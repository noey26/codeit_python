# 나머지
print(7 % 3)

# 거듭제곱
print(2 ** 3)

print(2.0 ** 3) # float > int

# 나눗셈 -> 항상 float형으로 나옴
print(7 / 2)
print(6 / 2)
print(7.0 / 2)
print(6.0 / 2.0)

# floor division (버림 나눗셈)
print(7 // 2)
print(8.0 // 3)

# round (반올림)
print(round(3.1415926535))
print(round(3.1415926535, 4))