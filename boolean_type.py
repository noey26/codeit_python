# 불린 (Boolean)
print(True and True)
print(True and False)
print(False and True)
print(False and False)
print(True or True)
print(True or False)
print(False or True)
print(False or False)

print(2 > 1)
print(2 < 1)
print(3 >= 2)
print(3 <= 3)
print(2 == 2)
print(2 != 2)

print("Hello" == "Hello")
print("Hello" != "Hello")

print(2 > 1 and "Hello" == "Hello") # True and True
print(not not False) # not True
print(not not True) # not False

print(type(3))
print(type(3.0))
print(type("3"))
print(type("True"))
print(type(True))
